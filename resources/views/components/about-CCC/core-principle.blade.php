                <li>
                    <span>
                        The CCC will operate as a national consensus group and will promote true partnership in the development and implementation of programs under Global Fund-supported grants.
                    </span>
                </li>
                <!-- #2  -->
                <li>
                    <span>
                        All members of the CCC will be treated as equal partners, with full rights to participation, expression, and involvement in decision-making.
                    </span>
                </li>
                <!-- #3 -->
                <li>
                    <span>
                        The CCC is broadly representative of all eligible and interested sectors.
                    </span>
                </li>
                <!-- #4 -->
                <li>
                    <span>
                        CCC members representing the non-government sectors are elected by their own constituencies using a transparent and documented election process.
                    </span>
                </li>
                <!-- #5  -->
                <li>
                    <span>
                        The CCC is fully accountable to all stakeholders and transparent in its decision-making and building public trust.
                    </span>
                </li>
                <!-- #6 -->
                <li>
                    <span>
                        The CCC shall promote linkages and consistency between Global Fund activities in Cambodia and the country's national development strategies; national disease strategies, and national efforts to increase the harmonization, coordination, and effectiveness of external assistance.
                    </span>
                </li>
                <!-- #7 -->
                <li>
                    <span>
                        The CCC adheres to the principles of: <br>
                        - broad and inclusive participation; <br>
                        - constituency representation; <br>
                        - democratic decision-making; <br>
                        - full transparency; and <br>
                        - cooperative partnership.
                    </span>
                </li>
                <!-- #8 -->
                <li>
                    <span>
                        The CCC will ensure that decisions on strategy and programming are informed by the best available evidence and prioritized according to local needs and to maximize impact of the available resources;
                    </span>
                </li>
                <!-- #9 -->
                <li>
                    <span>
                        Have sound policies and monitoring regarding conflicts of interest, and perceptions of conflicts of interest, between those allocating funding and those receiving funding;
                    </span>
                </li>
                <!-- #10-->
                <li>
                    <span>
                        Ensure that the CCC members and officials are accountable to stakeholders, by ensuring clarity of roles;
                    </span>
                </li>
                <!-- #11 -->
                <li>
                    <span>
                        Ensure that duplication of functions and consequent inefficiencies are avoided.
                    </span>
                </li>
