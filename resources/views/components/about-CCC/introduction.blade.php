                <li>
                    <span>
                        The Global Fund Grants to Cambodia have produced remarkable results in reducing the instances of HIV/AIDs, Tuberculosis and Malaria and strengthening the sustainability of the health system over the period from 2003 to 2017. Over these years, the capacity of the Royal Government of Cambodia (Government) and in particular the Ministry of Health (MOH), to implement the Global Fund Grants has increased significantly. There is greater ownership of the programs by the Government and MOH. The Government has put in place credible implementation systems, the most notable being the Government’s Standard Operating Procedures (SOPs) for all externally financed projects/programs in Cambodia comprising of the SOP Manual, the Procurement Manual and the Financial Management Manual.
                    </span>
