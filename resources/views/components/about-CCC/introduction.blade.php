                    <span>
                        The Cambodia Country Coordinating Committee (“CCC”)is a national multi-sectoral body founded in 2003 for coordinating the Global Fund supported activities in Cambodia. The Country Coordinating Committee was established as a mechanism for representation of the national stakeholder constituencies for the three diseases in Cambodia, and in line with the requirements and recommendations of the Global Fund. The CCC mechanism has been effective and contributed to better outcomes of Global Fund-funded programs and projects through effective coordination of Global Fund-related activities.
                    </span>
                </li>
